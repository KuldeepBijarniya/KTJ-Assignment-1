document.getElementById("rc1p1but").onclick = function myFunction() {
  var doc = document.getElementById("rc1p1but");
  if (doc.innerHTML=="Added✔") {
    doc.innerHTML = "Add+";
    doc.style.backgroundColor = "red";
  }
  else {
    doc.innerHTML = "Added✔";
    doc.style.backgroundColor = "green";
  }
}
